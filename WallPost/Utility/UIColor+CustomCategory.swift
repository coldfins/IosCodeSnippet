import UIKit

extension UIColor {
       
    class func colorLightgray() -> UIColor {
        return UIColor(red: 177/255.0, green: 221/255.0, blue: 226/255.0, alpha: 1)
    }
    
    class func colorForApp() -> UIColor {
        return UIColor(red: 47/255.0, green: 211/255.0, blue: 230/255.0, alpha: 1)
    }

   
}
