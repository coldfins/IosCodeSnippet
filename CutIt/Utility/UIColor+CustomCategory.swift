    }
    class func colorYellowText() -> UIColor {
        return UIColor(red: 238/255.0, green: 143/255.0, blue: 0/255.0, alpha: 1)
    }
    class func colorFooter() -> UIColor {
        return UIColor(red: 243/255.0, green: 239/255.0, blue: 239/255.0, alpha: 1)
    }
    class func colorBlueText() -> UIColor {
        return UIColor(red: 24/255.0, green: 119/255.0, blue: 187/255.0, alpha: 1)
    }
    class func colorLightGrayText() -> UIColor {
        return UIColor(red: 200/255.0, green: 200/255.0, blue: 205/255.0, alpha: 1)
    }
    class func colorDarkGrayText() -> UIColor {
