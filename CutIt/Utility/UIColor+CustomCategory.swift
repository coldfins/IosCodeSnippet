import UIKit

extension UIColor {
    
    class func colorRedText() -> UIColor {
        return UIColor(red: 236/255.0, green: 69/255.0, blue: 99/255.0, alpha: 1)
    }
    class func colorGrayText() -> UIColor {
        return UIColor(red: 220/255.0, green: 221/255.0, blue: 222/255.0, alpha: 1)
    }
    class func colorGreenText() -> UIColor {
