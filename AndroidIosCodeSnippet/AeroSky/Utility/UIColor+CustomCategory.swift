import UIKit

extension UIColor {
       
    class func colorLikeNavigationBackground() -> UIColor {
        return UIColor(red: 108/255.0, green: 197/255.0, blue: 75/255.0, alpha: 1)
    }
    
    class func colorLikeBG() -> UIColor {
        return UIColor(red: 238/255.0, green: 238/255.0, blue: 238/255.0, alpha: 1)
    }
    
    class func colorLikeNavigationBar() -> UIColor {
        return UIColor(red: 54/255.0, green: 191/255.0, blue: 252/255.0, alpha: 1)
    }
    
   
}
